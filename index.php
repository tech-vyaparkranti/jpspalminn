    $("#checkin").datepicker({
      minDate: today,
      dateFormat: 'yy-mm-dd',
      onSelect: function(selectedDate) {
        var checkinDate = new Date(selectedDate);
        checkinDate.setDate(checkinDate.getDate() + 1);
